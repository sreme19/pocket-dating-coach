      const l = updated.find((p: any) => p.category === 'linkedin') as any;
      careerHighlights = l?.insights?.length ? { insights: l.insights, aggregated: l.aggregated ?? '' } : null;
      pendingCareerInsights = [];
      editingCareerChips = false;
    }
