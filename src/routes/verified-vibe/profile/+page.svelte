  async function fetchCarImage(make: string, model: string) {
    const key = `${make}_${model}`.replace(/\s+/g, '_');
    if (key in carImages) return;
    carImages = { ...carImages, [key]: '' }; // mark as pending / no image yet
    try {
      // v3 prefix busts cached interior shots from the v2 Commons search
      const cacheKey = `vv_car_img_v3_${key}`;
      const cached = localStorage.getItem(cacheKey);
      if (cached) { carImages = { ...carImages, [key]: cached }; return; }
      const res = await fetch(`/api/verified-vibe/car-image?make=${encodeURIComponent(make)}&model=${encodeURIComponent(model)}`);
      if (res.ok) {
        const data = await res.json() as { imageUrl: string | null };
        if (data.imageUrl) {
          carImages = { ...carImages, [key]: data.imageUrl };
          localStorage.setItem(cacheKey, data.imageUrl);
        }
      }
    } catch { /* no image, SVG fallback stays */ }
  }

  async function uploadCarPhoto(make: string, model: string, file: File) {
    const key = `${make}_${model}`.replace(/\s+/g, '_');
    carUploadingKey = key;
    try {
      const fd = new FormData();
      fd.append('file', file);
      fd.append('make', make);
      fd.append('model', model);
      const res = await fetch('/api/verified-vibe/car-image', { method: 'POST', body: fd });
      const data = await res.json() as { imageUrl?: string; error?: string };
      if (data.imageUrl) {
        carImages = { ...carImages, [key]: data.imageUrl };
        // Bust old cache so fresh URL is used on next load
        localStorage.setItem(`vv_car_img_v3_${key}`, data.imageUrl);
      }
    } catch { /* non-critical */ } finally {
      carUploadingKey = null;
    }
  }

